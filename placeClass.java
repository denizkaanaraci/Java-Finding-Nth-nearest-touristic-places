
public class placeClass {
	
	private int x_coord;
	private int y_coord;
	private int fee;
	private int booster_dist;
	private int generation;
	private int value;
	
	
	public int getGeneration() {
		return generation;
	}
	public void setGeneration(int generation) {
		this.generation = generation;
	}
	public int getValue() {
		return value;
	}
	public void setValue(int value) {
		this.value = value;
	}
	public int getX_coord() {
		return x_coord;
	}
	public void setX_coord(int x_coord) {
		this.x_coord = x_coord;
	}
	public int getY_coord() {
		return y_coord;
	}
	public void setY_coord(int y_coord) {
		this.y_coord = y_coord;
	}
	public int getFee() {
		return fee;
	}
	public void setFee(int fee) {
		this.fee = fee;
	}
	public int getBooster_dist() {
		return booster_dist;
	}
	public void setBooster_dist(int booster_dist) {
		this.booster_dist = booster_dist;
	}
	
	

}
